def _normalize(text: str) -> str:
    return " ".join(text.lower().strip().split())


def get_profile(area_name: str) -> dict | None:
    if not area_name:
        return None

    normalized_query = _normalize(area_name)
    normalized_map = {_normalize(name): name for name in AREA_PROFILES}

    if normalized_query in normalized_map:
        return AREA_PROFILES[normalized_map[normalized_query]]

    partial_matches = [
        original_name
        for normalized_name, original_name in normalized_map.items()
        if normalized_query in normalized_name or normalized_name in normalized_query
    ]
    if partial_matches:
        partial_matches.sort(key=len)
        return AREA_PROFILES[partial_matches[0]]

    close = get_close_matches(
        normalized_query,
        list(normalized_map.keys()),
        n=1,
        cutoff=0.6,
    )
    if close:
        return AREA_PROFILES[normalized_map[close[0]]]

    return None
